    public void Cadastro(){
        String nome,descricao,tipo,fornecedor;
        int id;
        
        nome = txtnome.getText();
        descricao = txtdesc.getText();
        tipo = txttipo.getText();
        fornecedor = txtfornece.getText();
        id = Integer.parseInt(txtid.getText());
        
        Produto prof = new Produto(0,"","","","");
        prof.setNome(nome);
        prof.setDescricao(descricao);
        prof.setTipo(tipo);
        prof.setFornecedor(fornecedor);
        prof.setId(id);
        
        ProdutosCONEC prod = new ProdutosCONEC();
        prod.cadastrarProd(prof);
    }




